public class FirstJavaProgram {
    public static void main(String[] args){
        System.out.println(7);//정수 출력
        System.out.println(3.15); //실수 출력
        System.out.println("3+5 ="+8); //문자열+정수
        System.out.println(3.15+"는 실수입니다.");//실수+문자열
        System.out.println("3+5"+"의 연산 결과는 8입니다");//문자열+문자열
        System.out.println(3+5);//정수 계산식
        System.out.println("3+5");//문자열
        /*
        system.out.println
        1. 문자열을 출력하기 위해서는 큰따옴표로 묶어줘야 한다
        2. 큰 따옴표 업슨 것은 모두 변수명으로 인식한다
        3. 문자열과 변수명을 함께 사용할 수 있으나 반드시 '+'로 연결시켜줘야 한다
        4. 괄호안의 내용을 출력한 후 자동으로 줄바꾸는 기능이 있다
         */

    }
}
