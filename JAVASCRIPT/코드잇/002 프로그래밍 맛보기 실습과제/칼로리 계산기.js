// 칼로리 계산기

let espressoCalories = 10;
let milkCalories = 170;
let chocoSyrupCalories = 50;
let whippedCreamCalories = 60;

// 메뉴별 칼로리 테스트
console.log(espressoCalories); // 에스프레소 칼로리
console.log(espressoCalories + milkCalories); // 라떼 칼로리
console.log(espressoCalories + chocoSyrupCalories + milkCalories); // 모카 칼로리
console.log(espressoCalories + chocoSyrupCalories + milkCalories + whippedCreamCalories); // 모카(휘핑 추가) 칼로리